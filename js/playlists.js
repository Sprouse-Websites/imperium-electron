function SongFull() {
	document.getElementById("AllSongs").style.display = "block";
	document.getElementById("Playlist1").style.display = "none";
	document.getElementById("Playlist2").style.display = "none";
}

function ShowPlaylist1() {
	document.getElementById("AllSongs").style.display = "none";
	document.getElementById("Playlist1").style.display = "block";
	document.getElementById("Playlist2").style.display = "none";
}
